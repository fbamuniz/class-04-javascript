//Um array com nomes
const lista1 = ["João", "Maria", "José"];
console.log(lista1);

//Um array com lista de compras
const lista2 = ["Arroz", "Feijão", "Batata"];
console.log(lista2);

//Um array com nível acadêmico
const lista3 = ["Médio", "Técnico", "Superior"];
console.log(lista3);

//Concatenação entre duas listas
const lista_juntas1 = lista1.concat(lista2);
console.log(lista_juntas1);

//Concatenação entre três ou mais listas
const lista_juntas2 = lista1.concat(lista2, lista3);
console.log(lista_juntas2);